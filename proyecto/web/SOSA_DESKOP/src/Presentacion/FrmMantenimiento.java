    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        clsEMantenimiento objEC=new clsEMantenimiento();
        clsNmantenimiento objNC=new clsNmantenimiento();
        Date fecha = new Date();
        // Crear un formato de fecha con el idioma espa√±ol
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss", new Locale("es", "ES"));
        String fechaFormateada = formatoFecha.format(fecha);
        objEC.setId_maquina(Integer.parseInt(txtId.getText()));
        objEC.setEmp_con(Integer.parseInt(txtEncargado.getText()));
        objEC.setEstado(cmxEstado.getSelectedItem().toString());
        objEC.setObservaciones(txtObservaciones.getText());
        objEC.setFecha(txtFecha.getText());
        if (objNC.MtdModificarMantenimiento(objEC))
        {
            JOptionPane.showMessageDialog(null,"Datos Modificados Correctamente");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"ERROR");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

