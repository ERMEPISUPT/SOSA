    private void listar(){
        clsNmantenimiento objNC = new clsNmantenimiento();
        List<clsEMantenimiento> listaMaquinas = objNC.MtdListarMaquinas();

        DefaultTableModel model = (DefaultTableModel) tblMaquina.getModel();
        model.setRowCount(0);

        for (clsEMantenimiento producto : listaMaquinas) {
            Object[] row = {producto.getId_maquina(), producto.getEmp_con(), producto.getNumero(), producto.getTipo(), producto.getEstado(), producto.getObservaciones()};
            model.addRow(row);
        }
    }
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String dni = txtEncargado.getText().trim();

        // Verifica si el campo DNI est√° vac√≠o
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un DNI.");
            return;
        }

        // Llama al nuevo m√©todo buscarClientePorDNI de clsNcliente
        clsNmantenimiento negocioCliente = new clsNmantenimiento();
        clsEMantenimiento clienteEncontrado = negocioCliente.buscarEncargadoPorDNI(dni);

        // Verifica si se encontr√≥ al cliente
        if (clienteEncontrado != null) {
            // Muestra el nombre y apellido en el campo de texto de cliente
            txtBuscar.setText(clienteEncontrado.getNombre() + " " + clienteEncontrado.getApellido());
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron clientes con el DNI proporcionado.");
            txtBuscar.setText(""); // Limpia el campo de texto si no se encuentra un cliente.
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

