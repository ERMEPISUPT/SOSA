    private void tblMaquinaMouseClicked(java.awt.event.MouseEvent evt) {
        // Obtiene el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tblMaquina.getModel();

        // Obtiene el √≠ndice de la fila seleccionada
        int selectedRowIndex = tblMaquina.getSelectedRow();

        // Obtiene los datos de la fila seleccionada
        Object id = model.getValueAt(selectedRowIndex, 0);
        Object encargado = model.getValueAt(selectedRowIndex, 1);
        Object numero = model.getValueAt(selectedRowIndex, 2);
        Object tipo = model.getValueAt(selectedRowIndex, 3);
        Object estado = model.getValueAt(selectedRowIndex, 4);
        Object observaciones = model.getValueAt(selectedRowIndex, 5);
        Object fecha = model.getValueAt(selectedRowIndex, 6);
        

        // Muestra los datos en los JTextField
        txtId.setText(id.toString());
        txtEncargado.setText(encargado.toString());
        cmxEstado.setSelectedItem(estado.toString());
        txtObservaciones.setText(observaciones.toString());

        // Aqu√≠ puedes hacer lo mismo con otros JTextField usando los datos obtenidos
    }
