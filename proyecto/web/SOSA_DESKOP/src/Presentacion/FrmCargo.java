    private void listar() {
       clsNcargo objNC = new clsNcargo();
        // Assuming MtdListarCargo returns a List or array of cargo objects
        List<clsECargo> listaCargos = objNC.MtdListarCargo();

        DefaultTableModel model = (DefaultTableModel) tblCargo.getModel();
        model.setRowCount(0);

        for (clsECargo cargo : listaCargos) {
            Object[] row = {cargo.getCodigo(), cargo.getNombre(), "GUARDAR", "EDITAR", "ELIMINAR","BUSCAR","LISTAR"};
            model.addRow(row);
        }
        tblCargo.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent evt) {
            int row = tblCargo.rowAtPoint(evt.getPoint());
            int col = tblCargo.columnAtPoint(evt.getPoint());
            // Si se hace clic en el botón "GUARDAR"
            if (col == 2) {
                // Obtener el modelo de la tabla
                DefaultTableModel model = (DefaultTableModel) tblCargo.getModel();

                // Obtener la última fila (la que acabamos de agregar o editar)
                int lastRow = model.getRowCount() - 1;

                // Obtener los datos de la última fila
                int codigo = Integer.parseInt(model.getValueAt(lastRow, 0).toString());
                String nombre = model.getValueAt(lastRow, 1).toString();

                // Crear un objeto clsECargo con los datos obtenidos
                clsECargo nuevoCargo = new clsECargo();
                nuevoCargo.setCodigo(codigo);
                nuevoCargo.setNombre(nombre);

                // Guardar el nuevo cargo en la base de datos
                clsNcargo objNC = new clsNcargo();
                if (objNC.MtdGuardarCargo(nuevoCargo)) {
                    listar();
                } 
            }
            // Si se hace clic en el botón "EDITAR"
            if (col == 3) {
                // Obtener el modelo de la tabla
                DefaultTableModel model = (DefaultTableModel) tblCargo.getModel();

                // Obtener la última fila (la que acabamos de agregar o editar)
                int lastRow = model.getRowCount() - 1;

                // Obtener los datos de la última fila
                int codigo = Integer.parseInt(model.getValueAt(lastRow, 0).toString());
                String nombre = model.getValueAt(lastRow, 1).toString();

                // Crear un objeto clsECargo con los datos obtenidos
                clsECargo nuevoCargo = new clsECargo();
                nuevoCargo.setCodigo(codigo);
                nuevoCargo.setNombre(nombre);

                // Guardar el nuevo cargo en la base de datos
                clsNcargo objNC = new clsNcargo();
                if (objNC.MtdModificarCargo(nuevoCargo)) {
                    listar();
                } 
            }

            // Si se hace clic en el botón "ELIMINAR"
            if (col == 4) {
                clsECargo objEC = new clsECargo();
                clsNcargo objNC = new clsNcargo();
                objEC.setCodigo(Integer.parseInt(model.getValueAt(row, 0).toString()));
                if (objNC.MtdEliminarCargo(objEC)) {
                    listar();
                } 
            }
            if (col == 5) {
                clsNcargo objNC = new clsNcargo();
                String codigoNombre = JOptionPane.showInputDialog("Ingrese el código o nombre a buscar");

                // Assuming MtdBuscarCargo returns a List or array of cargo objects
                List<clsECargo> listaCargos = objNC.MtdBuscarCargo(codigoNombre);

                DefaultTableModel model = (DefaultTableModel) tblCargo.getModel();
                model.setRowCount(0);

                for (clsECargo cargo : listaCargos) {
                    Object[] row1 = {cargo.getCodigo(), cargo.getNombre(), "GUARDAR", "EDITAR", "ELIMINAR", "BUSCAR", "LISTAR"};
                    model.addRow(row1);
                }

                // Set the flag to true after showing the alert
                searchAlertShown = true;

                // Reset the flag in a short delay (you may adjust the delay based on your needs)
                Timer timer = new Timer(1000, e -> searchAlertShown = false);
                timer.setRepeats(false);
                timer.start();
            }
            if (col == 6) {
                listar();
            }
        }
    }); 
    }    
