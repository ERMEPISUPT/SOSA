    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCargo.getModel();
        // Agregar una nueva fila vacía
        model.addRow(new Object[]{null, null, "GUARDAR", "EDITAR", "ELIMINAR","BUSCAR","LISTAR"});

        // Obtener la última fila (la que acabamos de agregar)
        int lastRow = model.getRowCount() - 1;

        // Enfocar y empezar la edición en la celda de código (columna 0)
        tblCargo.requestFocus();
        tblCargo.editCellAt(lastRow, 0);
    }//GEN-LAST:event_btnNuevoActionPerformed

